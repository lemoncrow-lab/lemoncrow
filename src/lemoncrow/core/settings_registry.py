"""Registry of every user-tunable LemonCrow setting.

Each :class:`SettingSpec` binds a dotted CLI key (used by ``lc settings
show``/``set``) to the environment variable that actually drives runtime
behavior. This is the single source of truth consumed by
``lemoncrow.core.settings`` (persistence + env seeding) and the ``lc
settings`` CLI group.

Scope: this covers env vars read by Python code under ``src/`` — i.e. knobs
that can actually change behavior in a running LemonCrow process. Shell-only
install-script variables (``integrations/*/install.sh``) are out of scope
since they run before any Python process exists to seed them.

``default`` is informational only (shown by ``settings show`` when nothing is
persisted); the real runtime code paths already carry their own fallback and
are unaffected by what's recorded here. ``None`` means "LemonCrow picks an
internal default; see source for the exact value."

``settable=False`` marks values that are read-only through this surface —
either because they are generated per-run (swarm child ids, result paths) or
because they are the bootstrap knobs (``LEMONCROW_ROOT`` and friends) that this
very file's location depends on, so persisting them here would be circular.
They still show up in ``lc settings show`` for discoverability.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Any, Literal

SettingType = Literal["bool", "int", "float", "str"]


@dataclass(frozen=True)
class SettingSpec:
    key: str
    env_var: str | None
    type: SettingType
    default: Any
    category: str
    description: str
    settable: bool = True


